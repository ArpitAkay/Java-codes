package apnacollege;
public class C15_for_while_dowhileloops {
    public static void main(String args[]){
        //for loop
        for(int i=0;i<11;i++){
            System.out.print(i + " ");
        }

        //while loop
        int j = 0;
        while(j<11){
            System.out.print(j + " ");
            j++;
        }

        //do while loop
        int k = 0;
        do{
            System.out.print(k + " ");
            k++;
        }while(k<11);

    }
}